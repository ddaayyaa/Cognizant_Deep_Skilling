public class PrimitiveDataTypes {

    public static void main(String[] args) {

        byte age = 20;
        short year = 2026;
        int population = 100000;
        long mobile = 9876543210L;

        float percentage = 87.5f;
        double cgpa = 8.91;

        char section = 'A';

        boolean selected = true;

        System.out.println("Byte : " + age);
        System.out.println("Short : " + year);
        System.out.println("Int : " + population);
        System.out.println("Long : " + mobile);
        System.out.println("Float : " + percentage);
        System.out.println("Double : " + cgpa);
        System.out.println("Char : " + section);
        System.out.println("Boolean : " + selected);

    }
}